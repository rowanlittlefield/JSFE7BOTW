function UnitStats(
  level, experience, hp, strength, skill, speed, luck, defense,
  resistance, move, constitution, hp_growth_rate,
  strength_growth_rate, skill_growth_rate, speed_growth_rate,
  luck_growth_rate, defense_growth_rate, resistance_growth_rate,
  affinity
) {
  this.level = level;
  this.experience = experience;
  this.hp = hp;
  this.strength = strength;
  this.skill = skill;
  this.speed = speed;
  this.luck = luck;
  this.defense = defense;
  this.resistance = resistance;
  this.move = move;
  this.constitution = constitution;
  this.hp_growth_rate = hp_growth_rate;
  this.strength_growth_rate = strength_growth_rate;
  this.skill_growth_rate = skill_growth_rate;
  this.speed_growth_rate = speed_growth_rate;
  this.luck_growth_rate = luck_growth_rate;
  this.defense_growth_rate = defense_growth_rate;
  this.resistance_growth_rate = resistance_growth_rate;
  this.affinity = affinity;
}
